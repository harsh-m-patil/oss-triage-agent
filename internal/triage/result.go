package triage

import (
	"encoding/json"
	"fmt"
	"strings"
)

const disclaimer = "> *This was generated by AI during triage.*"

// Result is the machine-readable triage outcome parsed from agent output.
type Result struct {
	Category string `json:"category"`
	State    string `json:"state"`
	Close    bool   `json:"close"`
}

// ParseAgentOutput splits agent stdout into a comment body and structured triage result.
func ParseAgentOutput(raw string) (comment string, result Result, err error) {
	raw = strings.TrimSpace(raw)
	if raw == "" {
		return "", Result{}, fmt.Errorf("empty agent output")
	}

	jsonPayload, body := extractJSONPayload(raw)
	if jsonPayload == "" {
		return "", Result{}, fmt.Errorf("agent output missing json triage footer")
	}
	if err := json.Unmarshal([]byte(jsonPayload), &result); err != nil {
		return "", Result{}, fmt.Errorf("parse triage json: %w", err)
	}
	if !ValidCategory(result.Category) {
		return "", Result{}, fmt.Errorf("invalid triage category %q", result.Category)
	}
	if !ValidState(result.State) {
		return "", Result{}, fmt.Errorf("invalid triage state %q", result.State)
	}

	comment = strings.TrimSpace(body)
	comment = EnsureDisclaimer(comment)
	return comment, result, nil
}

func extractJSONPayload(raw string) (jsonPayload, body string) {
	const open = "```json"
	const close = "```"
	if start := strings.Index(raw, open); start >= 0 {
		rest := raw[start+len(open):]
		if end := strings.Index(rest, close); end >= 0 {
			payload := strings.TrimSpace(rest[:end])
			body = strings.TrimSpace(raw[:start])
			if tail := strings.TrimSpace(rest[end+len(close):]); tail != "" {
				if body != "" {
					body += "\n\n"
				}
				body += tail
			}
			return payload, body
		}
	}

	trimmed := strings.TrimSpace(raw)
	if strings.HasPrefix(trimmed, "{") && strings.HasSuffix(trimmed, "}") {
		return trimmed, ""
	}

	if idx := strings.LastIndex(raw, "{"); idx >= 0 {
		candidate := strings.TrimSpace(raw[idx:])
		if strings.HasSuffix(candidate, "}") {
			var probe Result
			if json.Unmarshal([]byte(candidate), &probe) == nil && ValidCategory(probe.Category) && ValidState(probe.State) {
				return candidate, strings.TrimSpace(raw[:idx])
			}
		}
	}
	return "", raw
}

// EnsureDisclaimer prepends the required AI triage disclaimer when missing.
func EnsureDisclaimer(body string) string {
	body = strings.TrimSpace(body)
	if body == "" {
		return disclaimer
	}
	if strings.Contains(body, disclaimer) {
		return body
	}
	return disclaimer + "\n\n" + body
}
