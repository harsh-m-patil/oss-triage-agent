package triage_test

import (
	"strings"
	"testing"

	"github.com/harsh-m-patil/oss-triage-agent/internal/triage"
)

func TestParseAgentOutput_withJSONFence(t *testing.T) {
	t.Parallel()

	raw := "## Agent Brief\n\nShip triage workflow.\n\n```json\n{\"category\":\"enhancement\",\"state\":\"ready-for-agent\",\"close\":false}\n```"
	comment, result, err := triage.ParseAgentOutput(raw)
	if err != nil {
		t.Fatalf("ParseAgentOutput: %v", err)
	}
	if result.Category != "enhancement" || result.State != "ready-for-agent" || result.Close {
		t.Fatalf("result = %+v", result)
	}
	if !strings.Contains(comment, "Ship triage workflow.") {
		t.Fatalf("comment = %q", comment)
	}
	if !strings.HasPrefix(comment, triageDisclaimerPrefix()) {
		t.Fatalf("comment missing disclaimer:\n%s", comment)
	}
}

func TestParseAgentOutput_withTrailingJSON(t *testing.T) {
	t.Parallel()

	raw := "## Triage Notes\n\nNeed logs.\n\n{\"category\":\"bug\",\"state\":\"needs-info\",\"close\":false}"
	_, result, err := triage.ParseAgentOutput(raw)
	if err != nil {
		t.Fatalf("ParseAgentOutput: %v", err)
	}
	if result.Category != "bug" || result.State != "needs-info" {
		t.Fatalf("result = %+v", result)
	}
}

func TestParseAgentOutput_rejectsInvalidCategory(t *testing.T) {
	t.Parallel()

	_, _, err := triage.ParseAgentOutput(`{"category":"feature","state":"ready-for-agent","close":false}`)
	if err == nil {
		t.Fatal("ParseAgentOutput: want error, got nil")
	}
}

func triageDisclaimerPrefix() string {
	return "> *This was generated by AI during triage.*"
}
